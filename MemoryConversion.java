public class MemoryConversion {
    
    // Method to convert Kilobytes to Megabytes
    public double kilobytesToMegabytes(double kilobytes) {
        return kilobytes / 1024;
    }

    // Method to convert Megabytes to Kilobytes
    public double megabytesToKilobytes(double megabytes) {
        return megabytes * 1024;
    }
}
